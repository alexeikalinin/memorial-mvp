# ─── Основная функция ──────────────────────────────────────────────────────────

async def run():
    from sqlalchemy.orm import sessionmaker
    SessionLocal = sessionmaker(bind=engine)
    db: Session = SessionLocal()

    try:
        # Убедимся, что есть пользователь owner_id=1
        user = db.query(User).filter(User.id == 1).first()
        if not user:
            user = User(id=1, email="demo@memorial.app", username="demo",
                        hashed_password="x", full_name="Demo User")
            db.add(user)
            db.commit()

        # ── Создаём мемориалы ──
        key_to_id: dict[str, int] = {}
        print("Создаём мемориалы...")
        for m in MEMORIALS:
            birth = datetime.strptime(m["birth"], "%Y-%m-%d") if m.get("birth") else None
            death = datetime.strptime(m["death"], "%Y-%m-%d") if m.get("death") else None

            memorial = Memorial(
                name=m["name"],
                description=m["desc"],
                birth_date=birth,
                death_date=death,
                is_public=True,
                voice_gender=m.get("voice_gender"),
                owner_id=1,
            )
            db.add(memorial)
            db.flush()
            key_to_id[m["key"]] = memorial.id
            print(f"  ✓ {memorial.name} (id={memorial.id})")

        db.commit()

        # ── Создаём воспоминания + embeddings ──
        print("\nСоздаём воспоминания и embeddings...")
        for key, memories_list in MEMORIES.items():
            memorial_id = key_to_id[key]
            for title, content in memories_list:
                memory = Memory(
                    memorial_id=memorial_id,
                    title=title,
                    content=content,
                    source="user",
                )
                db.add(memory)
                db.flush()

                # Создаём embedding
                try:
                    full_text = f"{title}: {content}" if title else content
                    embedding = await get_embedding(full_text)
                    embedding_id = await upsert_memory_embedding(
                        memory_id=memory.id,
                        memorial_id=memorial_id,
                        text=full_text,
                        embedding=embedding,
                        title=title,
                    )
                    memory.embedding_id = embedding_id
                    print(f"  ✓ [{key}] «{title}» — embedding создан")
                except Exception as e:
                    print(f"  ⚠ [{key}] «{title}» — embedding ошибка: {e}")

        db.commit()

        # ── Создаём семейные связи ──
        print("\nСоздаём семейные связи...")
        def add_rel(a_id: int, b_id: int, rel_type: RelationshipType):
            # Прямая связь
            existing = db.query(FamilyRelationship).filter(
                FamilyRelationship.memorial_id == a_id,
                FamilyRelationship.related_memorial_id == b_id,
                FamilyRelationship.relationship_type == rel_type,
            ).first()
            if not existing:
                db.add(FamilyRelationship(
                    memorial_id=a_id,
                    related_memorial_id=b_id,
                    relationship_type=rel_type,
                ))

            # Обратная связь
            reverse_map = {
                RelationshipType.PARENT: RelationshipType.CHILD,
                RelationshipType.CHILD: RelationshipType.PARENT,
                RelationshipType.SPOUSE: RelationshipType.SPOUSE,
                RelationshipType.SIBLING: RelationshipType.SIBLING,
            }
            rev_type = reverse_map[rel_type]
            existing_rev = db.query(FamilyRelationship).filter(
                FamilyRelationship.memorial_id == b_id,
                FamilyRelationship.related_memorial_id == a_id,
                FamilyRelationship.relationship_type == rev_type,
            ).first()
            if not existing_rev:
                db.add(FamilyRelationship(
                    memorial_id=b_id,
                    related_memorial_id=a_id,
                    relationship_type=rev_type,
                ))

        type_map = {
            "parent": RelationshipType.PARENT,
            "child": RelationshipType.CHILD,
            "spouse": RelationshipType.SPOUSE,
            "sibling": RelationshipType.SIBLING,
        }

        for (from_key, to_key, rel_type_str) in RELATIONSHIPS:
            a_id = key_to_id[from_key]
            b_id = key_to_id[to_key]
            add_rel(a_id, b_id, type_map[rel_type_str])
            print(f"  ✓ {from_key} → {to_key} ({rel_type_str})")

        db.commit()

        print(f"\n✅ Готово!")
        print(f"   Мемориалов: {len(MEMORIALS)}")
        print(f"   Воспоминаний: {sum(len(v) for v in MEMORIES.values())}")
        print(f"   Связей: {len(RELATIONSHIPS) * 2} (двунаправленных)")
        print()
        print("Ключевые неочевидные связи для демо:")
        print(f"  Виктор Ковалёв (id={key_to_id['viktor']}) ←4 хопа→ Анна Морозова (id={key_to_id['anna']})")
        print(f"  Полина Власова (id={key_to_id['polina']}) ←через Ирину→ род Морозовых")
        print()
        print("Проверь в браузере:")
        print(f"  /memorials/{key_to_id['viktor']} → Семейное дерево → Найти скрытые связи")

    finally:
        db.close()


if __name__ == "__main__":
    asyncio.run(run())

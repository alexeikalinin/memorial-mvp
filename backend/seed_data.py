import sys
import requests

import os
BASE_URL = os.environ.get("SEED_BASE_URL", "http://localhost:8000/api/v1")


# ──────────────────────────────────────────────
# Helpers
# ──────────────────────────────────────────────

def create_memorial(name, description, birth_date, death_date, is_public=True) -> int:
    r = requests.post(f"{BASE_URL}/memorials/", json={
        "name": name,
        "description": description,
        "birth_date": f"{birth_date}T00:00:00" if birth_date else None,
        "death_date": f"{death_date}T00:00:00" if death_date else None,
        "is_public": is_public,
    })
    if not r.ok:
        print(f"ERROR creating memorial '{name}': {r.status_code} {r.text}")
        sys.exit(1)
    mid = r.json()["id"]
    print(f"  + [{mid}] {name}")
    return mid


def add_memory(memorial_id: int, title: str, content: str, event_date: str = None):
    payload = {"title": title, "content": content}
    if event_date:
        payload["event_date"] = f"{event_date}T00:00:00"
    r = requests.post(f"{BASE_URL}/memorials/{memorial_id}/memories", json=payload)
    if not r.ok:
        print(f"  WARNING: memory '{title}' -> {r.status_code} {r.text}")


def link(a_id: int, b_id: int, rel_type: str, notes: str = None):
    payload = {"related_memorial_id": b_id, "relationship_type": rel_type}
    if notes:
        payload["notes"] = notes
    r = requests.post(f"{BASE_URL}/family/memorials/{a_id}/relationships", json=payload)
    if not r.ok and "already exists" not in r.text:
        print(f"  WARNING: link {a_id}->{b_id} ({rel_type}): {r.text}")


# ──────────────────────────────────────────────
# Проверка сервера
# ──────────────────────────────────────────────
print("Проверяем сервер...")

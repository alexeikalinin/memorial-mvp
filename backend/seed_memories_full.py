async def run():
    SessionLocal = sessionmaker(bind=engine)
    db: Session = SessionLocal()

    try:
        # Убедимся, что есть пользователь owner_id=1
        user = db.query(User).filter(User.id == 1).first()
        if not user:
            user = User(id=1, email="demo@memorial.app", username="demo",
                        hashed_password="x", full_name="Demo User")
            db.add(user)
            db.commit()

        # ── Находим мемориалы по имени и добавляем воспоминания ──
        total_created = 0
        total_skipped = 0

        for person_name, memories_list in ALL_MEMORIES.items():
            memorial = db.query(Memorial).filter(
                Memorial.name == person_name
            ).first()

            if not memorial:
                print(f"  ⚠ Мемориал не найден: {person_name!r} — пропускаем")
                continue

            print(f"\n[{memorial.id}] {memorial.name} — добавляем {len(memories_list)} воспоминаний")

            for title, content in memories_list:
                # Проверяем, нет ли уже такого воспоминания (по заголовку)
                existing = db.query(Memory).filter(
                    Memory.memorial_id == memorial.id,
                    Memory.title == title,
                ).first()
                if existing:
                    print(f"  → уже есть: «{title}»")
                    total_skipped += 1
                    continue

                memory = Memory(
                    memorial_id=memorial.id,
                    title=title,
                    content=content,
                    source="user",
                )
                db.add(memory)
                db.flush()

                # Создаём embedding
                try:
                    full_text = f"{title}: {content}"
                    embedding = await get_embedding(full_text)
                    embedding_id = await upsert_memory_embedding(
                        memory_id=memory.id,
                        memorial_id=memorial.id,
                        text=full_text,
                        embedding=embedding,
                        title=title,
                    )
                    memory.embedding_id = embedding_id
                    print(f"  ✓ «{title}»")
                    total_created += 1
                except Exception as e:
                    print(f"  ⚠ «{title}» — embedding ошибка: {e}")
                    total_created += 1  # Воспоминание всё равно создаём

            db.commit()

        print(f"\n✅ Готово!")
        print(f"   Воспоминаний создано: {total_created}")
        print(f"   Воспоминаний пропущено (дубли): {total_skipped}")
        print()
        print("Совет: после наполнения запусти синхронизацию семейных воспоминаний")
        print("  POST /api/v1/ai/family/sync-memories/{memorial_id}")
        print("  чтобы перекрёстные упоминания автоматически распространились между родственниками.")

    finally:
        db.close()


if __name__ == "__main__":
    asyncio.run(run())
